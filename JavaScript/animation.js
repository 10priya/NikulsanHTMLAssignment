
$(window).scroll(function() {
    $('#animate').each(function(){
        if ($(this).is(":in-viewport")) {
            const element1 = document.querySelector('#info1')
            element1.classList.add('animated', 'fadeInUp')
        } else {
            const element1 = document.querySelector('#info1')
            element1.classList.remove('animated', 'fadeInUp')
        }
    })
});
    
